# Kommentarzeilen die NiCo vor die nico-version-Zeile setzt
_NICO_HEADER = (
    "# Generated by NiCo – NixOS Configurator\n"
    "# Do not edit manually. Use NiCo or add custom Nix via Nix-Brix.\n"
)


_VERSION_ANY_RE = re.compile(
    r'^# nico-version: (?:[a-z]+#)?[0-9a-f]{8}\n?', re.MULTILINE
)


def _set_type_in_content(content: str, ftype: str) -> str:
    """Replace or insert the type code in the nico-version line, recomputing the hash.

    Handles all three cases:
      - type already present  → remove old line, recompute hash, write new line
      - no type, has hash     → remove old line, recompute hash, write new line
      - no nico-version line  → prepend nico-version line (+ NiCo header for CO only)
    """
    if _VERSION_ANY_RE.search(content):
        # Remove the existing nico-version line to get canonical content for hashing
        canonical = _VERSION_ANY_RE.sub('', content, count=1)
        h = hashlib.sha256(canonical.encode()).hexdigest()[:8]
        new_line = f"# nico-version: {ftype}#{h}\n"
        return _VERSION_ANY_RE.sub(new_line, content, count=1)
    # Keine nico-version-Zeile → nico-version-Zeile an den Anfang
    # CO-Dateien erhalten zusätzlich den _NICO_HEADER.
    # Wichtig: Der Hash muss auf dem Canonical berechnet werden, also dem was
    # check_version_hash als Canonical sieht: alles außer der nico-version-Zeile.
    # Da _NICO_HEADER in der Datei VOR der nico-version-Zeile steht, ist
    # Canonical für co-Dateien = _NICO_HEADER + content.
    if ftype == 'co':
        canonical = _NICO_HEADER + content
        h = hashlib.sha256(canonical.encode()).hexdigest()[:8]
        nico_line = f"# nico-version: {ftype}#{h}\n"
        return f"{_NICO_HEADER}{nico_line}{content}"
    h = hashlib.sha256(content.encode()).hexdigest()[:8]
    nico_line = f"# nico-version: {ftype}#{h}\n"
    return f"{nico_line}{content}"


# Filesystem prefixes that NiCo must never write to
_FORBIDDEN_PATH_PREFIXES = [
    Path('/etc'), Path('/usr'), Path('/bin'), Path('/sbin'),
    Path('/lib'), Path('/lib64'), Path('/proc'), Path('/sys'),
    Path('/dev'), Path('/run'), Path('/boot'), Path('/nix/store'),
    Path('/root'), Path('/var'),
]


def _clean_nix_error(raw: str, tmpfile: str) -> str:
    """
    Make nix-instantiate error output more readable:
      - Replace the temp file path with '<config>'
      - Strip leading 'error:' boilerplate lines that repeat the file path
    """
    import re as _re
    cleaned = raw.replace(tmpfile, "<config>")
    # Remove the first line if it is just "error:" or a bare path reference
    lines = cleaned.splitlines()
    # Remove leading blank lines
    while lines and not lines[0].strip():
        lines.pop(0)
    return "\n".join(lines)


def _validate_user_path(raw: str):
    """
    Validate a user-provided filesystem path.
    Returns (resolved_path, None) on success or (None, error_str) on failure.
    Blocks path traversal (via Path.resolve) and known system directories.
    """
    if not raw or not raw.strip():
        return None, "ERR_NO_DIR"
    try:
        p = Path(raw).expanduser().resolve()
    except Exception:
        return None, "ERR_INVALID_PATH"
    for prefix in _FORBIDDEN_PATH_PREFIXES:
        try:
            p.relative_to(prefix)
            return None, "ERR_SYSTEM_PATH"
        except ValueError:
            pass
    return p, None


def _scan_flake_hosts(flake_path: Path) -> list[str]:
    """
    Scan an external flake.nix for nixosConfigurations host names.
    Used only when NiCo has no internal knowledge of the hosts (external file).
    Simple line-by-line scan – no full Nix parser needed for this pattern.
    """
    try:
        content = flake_path.read_text(encoding="utf-8")
    except OSError:
        return []
    # Match: nixosConfigurations.hostname  or  nixosConfigurations."hostname"
    found: list[str] = []
    for m in re.finditer(r'nixosConfigurations\s*\.\s*"?([\w-]+)"?', content):
        h = m.group(1)
        if h not in found:
            found.append(h)
    return found


def _get_flake_hosts(nixos_dir: str, data: dict) -> list[str]:
    """
    Return all flake host names for rebuild.
    Primary source is the current flake.nix on disk so dry-run matches the
    host picker. Exotic Brix hosts are added as a fallback.
    """
    hosts: list[str] = []

    flake_path = Path(nixos_dir) / "flake.nix"
    if flake_path.exists():
        try:
            flake_data = dict(data)
            flake_data.update(importer.parse_flake_nix(flake_path.read_text(encoding="utf-8")))
            for h in flake_data.get("flake_hosts") or []:
                name = h.get("name", "")
                if name and name not in hosts:
                    hosts.append(name)
        except OSError:
            pass

    # Fallback: panel-managed hosts from already loaded data
    for h in data.get("flake_hosts") or []:
        name = h.get("name", "")
        if name and name not in hosts:
            hosts.append(name)

    # Exotic host names from Brix blocks in flake.nix
    if flake_path.exists():
        try:
            brix = extract_brick_blocks(flake_path.read_text(encoding="utf-8"))
            for bname, block in brix.items():
                if block.get("section") == "Outputs-Hosts" and bname not in hosts:
                    hosts.append(bname)
                elif bname == "outputs-declaration" and block.get("section") == "Outputs":
                    # Legacy outputs-declaration: scan inside for host names
                    body = _brick_body(block["text"])
                    for m in re.finditer(r'\b([\w-]+)\s*=\s*nixpkgs\.lib\.nixosSystem', body):
                        h = m.group(1)
                        if h not in hosts:
                            hosts.append(h)
        except OSError:
            pass

    if not hosts:
        single = (data.get("hostname") or "nixos").strip()
        if single:
            hosts.append(single)

    return hosts or ["nixos"]


def _flake_outputs_modify_host(brix_text: str, action: str, host_name: str) -> str | None:
    """Add or remove a nixosSystem host entry inside the nixosConfigurations block
    of an outputs-declaration Brix body.  Returns the modified body or None."""
    body = _brick_body(brix_text)

    # Locate nixosConfigurations = { ... }
    m = re.search(r'\bnixosConfigurations\s*=\s*\{', body)
    if not m:
        return None
    brace_start = body.index('{', m.start())
    depth, i, cfg_end = 0, brace_start, len(body)
    while i < len(body):
        if body[i] == '{':
            depth += 1
        elif body[i] == '}':
            depth -= 1
            if depth == 0:
                cfg_end = i
                break
        i += 1

    cfg_body = body[brace_start + 1:cfg_end]

    def _extract_system_block(text: str, sys_match) -> tuple[int, int]:
        """Return (start, end) of the full '<name> = nixpkgs.lib.nixosSystem { ... };'."""
        bp = text.index('{', sys_match.end() - 1)
        d, j, end = 0, bp, len(text)
        while j < len(text):
            if text[j] == '{':
                d += 1
            elif text[j] == '}':
                d -= 1
                if d == 0:
                    end = j + 1
                    while end < len(text) and text[end] in ' \t':
                        end += 1
                    if end < len(text) and text[end] == ';':
                        end += 1
                    break
            j += 1
        return sys_match.start(), end

    if action == 'add':
        sys_m = re.search(
            r'\s+[\w-]+\s*=\s*nixpkgs\.lib\.nixosSystem\s*\{', cfg_body
        )
        if not sys_m:
            return None
        src_name = re.search(r'([\w-]+)\s*=\s*nixpkgs', sys_m.group()).group(1)
        start_pos, end_pos = _extract_system_block(cfg_body, sys_m)
        template_block = cfg_body[start_pos:end_pos]
        new_block = (
            template_block
            .replace(f'{src_name} =', f'{host_name} =', 1)
            .replace(f'./hosts/{src_name}/', f'./hosts/{host_name}/')
            .replace(f'./hosts/{src_name}', f'./hosts/{host_name}')
        )
        new_cfg_body = cfg_body.rstrip('\n') + '\n' + new_block + '\n'

    elif action == 'remove':
        sys_m = re.search(
            r'\s+' + re.escape(host_name) + r'\s*=\s*nixpkgs\.lib\.nixosSystem\s*\{',
            cfg_body,
        )
        if not sys_m:
            return None
        start_pos, end_pos = _extract_system_block(cfg_body, sys_m)
        new_cfg_body = cfg_body[:start_pos] + cfg_body[end_pos:]
    else:
        return None

    return body[:brace_start + 1] + new_cfg_body + body[cfg_end:]


def _modify_brick_in_file(
    nixos_dir: str,
    fname: str,
    modifier,   # callable: dict[str, dict] -> dict[str, dict]
) -> tuple[bool, str]:
    """
    Read fname, apply modifier(blocks) → new_blocks, strip old brick markers,
    inject new_blocks, recompute hash, write back.
    Returns (success, error_key_or_empty).
    """
    fpath = Path(nixos_dir) / fname
    if not fpath.exists():
        return False, "ERR_FILE_NOT_FOUND"
    try:
        content = fpath.read_text(encoding="utf-8")
    except OSError:
        return False, "ERR_READ"

    ftype = _get_nico_type(content)
    if ftype is None or ftype == "":
        ftype = "nd"

    blocks     = extract_brick_blocks(content)
    new_blocks = modifier(blocks)
    clean      = strip_brick_blocks(content)
    with_brick = inject_brick_blocks(clean, new_blocks)
    final      = _set_type_in_content(with_brick, ftype)

    try:
        fpath.write_text(final, encoding="utf-8")
    except OSError:
        return False, "ERR_WRITE"

    return True, ""


def _brick_body(text: str) -> str:
    """Extract body lines from a formatted brick text (strips start/end marker lines)."""
    lines = text.splitlines(keepends=True)
    if len(lines) >= 2:
        return "".join(lines[1:-1])
    return ""


def create_app() -> Flask:
    app = Flask(__name__)
    # Preserve dict insertion order in JSON responses (needed for brick_blocks ordering)
    app.json.sort_keys = False

    # One CSRF token per server process – regenerated on each start
    _csrf_token = secrets.token_hex(32)

    # Sudo-Passwort-Nonces: nonce → (password, expiry_timestamp)
    # POST /api/sudo/acquire speichert, rebuild_stream/symlink lesen einmalig.
    import time as _time_mod
    _sudo_nonces: dict[str, tuple[str, float]] = {}

    # ------------------------------------------------------------------ helpers

    def _nixos_dir() -> str | None:
        return config_manager.get_nixos_config_dir()

    def _require_setup():
        """Return (nixos_dir, None) or (None, error_response)."""
        d = _nixos_dir()
        if not d:
            return None, (jsonify({"error": "ERR_NOT_SETUP"}), 400)
        return d, None

    def _check_csrf():
        """Return None if the CSRF token is valid, or an error response tuple."""
        token = request.headers.get('X-CSRF-Token', '')
        if not secrets.compare_digest(token, _csrf_token):
            return jsonify({"error": "ERR_CSRF"}), 403
        return None

    # ------------------------------------------------------------------ routes

    # Cache-buster: changes every server restart so browsers always reload static files
    _static_v = secrets.token_hex(6)

    @app.route("/")
    def index():
        from flask import make_response
        r = make_response(render_template(
            "index.html",
            csrf_token=_csrf_token,
            home_dir=str(Path.home()),
            static_v=_static_v,
        ))
        r.headers["Cache-Control"] = "no-store"
        return r

    @app.route("/api/langs")
    def available_langs():
        """Return sorted list of available language codes (scanned from static/lang/)."""
        lang_dir = Path(__file__).parent / "static" / "lang"
        codes = sorted(p.stem for p in lang_dir.glob("*.json"))
        return jsonify(codes)

    @app.route("/api/status")
    def status():
        nixos_dir = _nixos_dir()
        if nixos_dir and Path(nixos_dir).is_dir():
            p = Path(nixos_dir)
            has_config = (p / "configuration.nix").exists() or (p / "flake.nix").exists()
            return jsonify({
                "setup_complete":    True,
                "needs_import":      not has_config,
                "nixos_config_dir":  nixos_dir,
            })
        return jsonify({"setup_complete": False})

    @app.route("/api/settings/dir")
    def settings_dir():
        """Return the current NixOS config directory."""
        return jsonify({"dir": _nixos_dir() or ""})

    @app.route("/api/setup", methods=["POST"])
    def setup():
        if err := _check_csrf(): return err

        data = request.get_json(silent=True) or {}
        raw = data.get("nixos_config_dir", "").strip()
        create_if_missing = bool(data.get("create_if_missing", False))

        path, path_err = _validate_user_path(raw)
        if path_err:
            return jsonify({"error": path_err}), 400

        dir_created = False
        if not path.exists():
            if not create_if_missing:
                return jsonify({"needs_confirmation": True, "path": str(path)})
            try:
                path.mkdir(parents=True)
                dir_created = True
            except OSError:
                return jsonify({"error": "ERR_DIR_CREATE"}), 400
        elif not path.is_dir():
            return jsonify({"error": "ERR_NOT_A_DIR"}), 400

        config_manager.init_nico_dir(str(path))
        config_manager.migrate_nico_json(str(path))  # Migrate old nico.json → config.json
        config_manager.save_app_settings({"nixos_config_dir": str(path)})

        # Auto-copy hardware-configuration.nix from /etc/nixos/ if available
        hw_copied = False
        hw_src    = Path("/etc/nixos/hardware-configuration.nix")
        hw_dst    = path / "hardware-configuration.nix"
        # Copy only if source exists and destination doesn't (never overwrite)
        if hw_src.is_file() and not hw_dst.exists():
            try:
                import shutil as _shutil
                _shutil.copy2(hw_src, hw_dst)
                hw_copied = True
            except OSError:
                pass  # permission denied or other OS error – silently skip

        # Set hardware_config flag if hardware-configuration.nix is present
        if hw_dst.exists():
            cfg = config_manager.load_config(str(path)) or {}
            cfg["hardware_config"] = True
            config_manager.save_config(str(path), cfg)

        # Ensure git repo exists – init if missing (best-effort, silently skip if git unavailable)
        try:
            if not git_manager.is_git_repo(str(path)):
                git_manager.init_repo(str(path))
        except Exception:
            pass

        return jsonify({
            "success":          True,
            "nixos_config_dir": str(path),
            "hw_copied":        hw_copied,
            "hw_present":       hw_dst.exists(),
            "dir_created":      dir_created,
        })

    @app.route("/api/fs/ls")
    def fs_ls():
        """Return subdirectories of a given path for the directory browser."""
        raw = request.args.get("path", str(Path.home()))
        try:
            p = Path(raw).expanduser().resolve()
        except Exception:
            return jsonify({"error": "ERR_INVALID_PATH"}), 400
        if not p.is_dir():
            # Try parent so the browser can still navigate
            p = p.parent
        try:
            dirs = sorted(
                entry.name for entry in p.iterdir() if entry.is_dir()
            )
        except PermissionError:
            dirs = []
        parent = str(p.parent) if p != p.parent else None
        return jsonify({"path": str(p), "parent": parent, "dirs": dirs})

    @app.route("/api/config", methods=["GET"])
    def get_config():
        nixos_dir, err = _require_setup()
        if err:
            return err
        data = config_manager.load_config(nixos_dir)

        # Datei ist die Wahrheit – nico.json ist nur Arbeitshilfe für Formfelder.
        # Brix-Blöcke leben ausschließlich in den .nix-Dateien, nie in nico.json.
        # Externe Änderungen werden sofort normalisiert und zurückgeschrieben.
        cfg_nix = Path(nixos_dir) / "configuration.nix"
        if cfg_nix.exists():
            try:
                nix_content = cfg_nix.read_text(encoding="utf-8")
                hash_status = generator.check_version_hash(nix_content)
                if data is None:
                    data = config_manager.get_defaults()
                if hash_status in ("modified", "no_hash"):
                    # Datei wurde extern verändert: Brick-Blöcke (inkl. altes brix-Format)
                    # zuerst extrahieren, dann erkannte Felder parsen,
                    # verbleibenden Rest als Brick sichern und Datei neu schreiben.
                    existing_bricks = extract_brick_blocks(nix_content)
                    nix_without_bricks = strip_brick_blocks(nix_content)
                    recognized = importer.parse_config(nix_without_bricks)
                    data.update(recognized)
                    config_manager.save_config(nixos_dir, data)
                    rest = importer.build_rest_brix(nix_without_bricks, recognized)
                    blocks = dict(existing_bricks)
                    if rest.strip():
                        rest_blocks = brix_content_to_bricks(rest, section="Start", existing_blocks=blocks)
                        blocks.update(rest_blocks)
                    data["brick_blocks"] = blocks
                    cfg_nix.write_text(
                        generator.generate_configuration_nix(data), encoding="utf-8"
                    )
                else:
                    # Datei unverändert: Brix frisch aus Datei lesen (nie aus nico.json)
                    data["brick_blocks"] = extract_brick_blocks(nix_content)
            except OSError:
                pass

        # Keine parsierbare .nix → Frischstart
        if data is None:
            config_manager.init_nico_dir(nixos_dir)
            data = config_manager.load_config(nixos_dir)

        # Migrate old nico.json to config.json if present
        config_manager.migrate_nico_json(nixos_dir)

        # flake.nix immer parsen – Datei ist die Wahrheit für flake_*-Felder.
        # (analog configuration.nix; flake_* werden nie vom Hauptformular geliefert)
        flake_nix_path = Path(nixos_dir) / "flake.nix"
        if flake_nix_path.exists():
            try:
                flake_content = flake_nix_path.read_text(encoding="utf-8")
                parsed = importer.parse_flake_nix(flake_content)
                data.update(parsed)
                config_manager.save_config(nixos_dir, data)
                # Auto-Migration: gestempelte aber nicht generierte flake.nix
                # (nico-version Header vorhanden, aber kein "# Generated by NiCo")
                # → Brix-Blöcke extrahieren und sauber neu generieren.
                # Einmalig – danach hat die Datei "# Generated by NiCo" und
                # diese Bedingung trifft nie wieder zu.
                if _get_nico_type(flake_content) is not None and \
                        "# Generated by NiCo" not in flake_content:
                    flake_brix = importer.build_flake_brix(flake_content)
                    data["flake_brick_blocks"] = flake_brix
                    flake_nix_path.write_text(
                        generator.generate_flake_nix(data, nixos_dir=nixos_dir),
                        encoding="utf-8",
                    )
                    flake_content = flake_nix_path.read_text(encoding="utf-8")
                data["flake_brick_blocks"] = extract_brick_blocks(flake_content)
            except OSError:
                pass

        home_nix_path = Path(nixos_dir) / "home.nix"
        if home_nix_path.exists():
            try:
                home_content = home_nix_path.read_text(encoding="utf-8")
                hash_status = generator.check_version_hash(home_content)
                if _get_nico_type(home_content) is not None and hash_status in ("modified", "no_hash"):
                    existing_bricks = extract_brick_blocks(home_content)
                    home_without_bricks = strip_brick_blocks(home_content)
                    hm = importer.parse_home_config(home_without_bricks)
                    data["home_manager"] = hm
                    rest = importer.build_home_rest_brix(home_without_bricks, hm)
                    blocks = dict(existing_bricks)
                    if rest.strip():
                        rest_blocks = brix_content_to_bricks(rest, section="End", existing_blocks=blocks)
                        blocks.update(rest_blocks)
                    data["hm_brick_blocks"] = blocks
                    home_data = dict(hm)
                    home_data["hm_brick_blocks"] = blocks
                    home_nix_path.write_text(
                        hm_generator.generate_home_nix(home_data), encoding="utf-8"
                    )
                else:
                    data["hm_brick_blocks"] = extract_brick_blocks(home_content)
            except OSError:
                pass

        # Check integrity of managed files (computed, not stored in nico.json)
        modified = []
        for fname in ("configuration.nix", "flake.nix", "home.nix"):
            p = Path(nixos_dir) / fname
            if p.exists():
                result = generator.check_version_hash(p.read_text())
                if result == "modified":
                    modified.append(fname)
        data["_modified_files"] = modified
        return jsonify(data)

    @app.route("/api/config", methods=["POST"])
    def save_config():
        if err := _check_csrf(): return err
        nixos_dir, err = _require_setup()
        if err:
            return err

        incoming = request.get_json(silent=True) or {}
        existing = config_manager.load_config(nixos_dir) or {}

        # These fields are managed by dedicated endpoints – never overwrite from the form
