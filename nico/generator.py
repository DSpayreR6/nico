def _user_block_lines(name: str, description: str, uid: str, password: str,
                      groups: "list[str]", shell: str, extra_nix: str) -> "list[str]":
    groups_str = " ".join(f'"{nix_esc(g)}"' for g in groups)
    lines = [f"  users.users.{nix_attr(name)} = {{", "    isNormalUser = true;"]
    if description:
        lines += [f'    description = "{nix_esc(description)}";']
    if uid.isdigit():
        lines += [f"    uid = {uid};"]
    if password:
        lines += [f'    initialPassword = "{nix_esc(password)}";']
    lines += [f"    extraGroups = [ {groups_str} ];"]
    if shell != "bash":
        shell_pkg = _SHELL_PKGS.get(shell, shell)
        lines += [f"    shell = pkgs.{shell_pkg};"]
    for ln in extra_nix.splitlines():
        if ln.strip():
            lines += [f"    {ln.strip()}"]
    lines += ["  };"]
    return lines


def _sec_users(data: dict, brix: dict) -> "list[str]":
    username = data.get("username", "")
    if not username:
        return [_section("Benutzer")] if _has_section_brix(brix, "Benutzer") else []
    user_groups_list  = data.get("user_groups", ["wheel", "networkmanager"])
    user_groups_extra = [g for g in (data.get("user_groups_extra") or "").split() if g]
    user_all_groups   = sorted(set(user_groups_list + user_groups_extra)) or ["wheel"]
    lines = [_section("Benutzer")]
    lines += _user_block_lines(
        username,
        data.get("user_description", ""),
        str(data.get("user_uid") or "").strip(),
        data.get("user_initial_password", ""),
        user_all_groups,
        data.get("user_shell", "bash") or "bash",
        (data.get("user_extra_nix") or "").strip(),
    )
    if data.get("guest_user", False):
        # Guest user block (no password, no sudo)
        lines += [
            "  users.users.gast = {",
            "    isNormalUser = true;",
            '    initialPassword = "";',
            "  };",
        ]
    return lines


def _sec_extra_users(data: dict) -> "list[str]":
    lines = []
    for eu in data.get("extra_users", []):
        eu_name = (eu.get("username") or "").strip()
        if not eu_name:
            continue
        lines += _user_block_lines(
            eu_name,
            (eu.get("description") or "").strip(),
            str(eu.get("uid") or "").strip(),
            (eu.get("initial_password") or "").strip(),
            eu.get("groups") or ["wheel", "networkmanager"],
            (eu.get("shell") or "bash").strip(),
            (eu.get("extra_nix") or "").strip(),
        )
    return lines


def _sec_programs(data: dict, brix: dict) -> "list[str]":
    prog_lines = []
    if data.get("allowUnfree"):
        prog_lines += ["  nixpkgs.config.allowUnfree = true;"]
    if data.get("steam"):
        prog_lines += ["  programs.steam.enable = true;"]
    if data.get("appimage"):
        prog_lines += [
            "  programs.appimage = {",
            "    enable = true;",
            "    binfmt = true;",
            "  };",
        ]
    if data.get("firefox"):
        ff_lang  = data.get("firefox_lang_packs", "")
        ff_prefs = data.get("firefox_prefs", "")
        lang_list  = [p.strip() for p in ff_lang.split(",") if p.strip()] if ff_lang else []
        pref_lines = [l.strip() for l in ff_prefs.splitlines() if l.strip()] if ff_prefs else []
        if lang_list or pref_lines:
            prog_lines += ["  programs.firefox = {", "    enable = true;"]
            if lang_list:
                packs = " ".join(f'"{nix_esc(p)}"' for p in lang_list)
                prog_lines += [f"    languagePacks = [ {packs} ];"]
            if pref_lines:
                prog_lines += ["    preferences = {"]
                for pl in pref_lines:
                    prog_lines += [f"      {pl}"]
                prog_lines += ["    };"]
            prog_lines += ["  };"]
        else:
            prog_lines += ["  programs.firefox.enable = true;"]
    if data.get("flatpak_enable"):
        prog_lines += ["  services.flatpak.enable = true;"]
    flatpak_remotes = [r for r in (data.get("flatpak_remotes") or []) if r.get("name") and r.get("url")]
    if flatpak_remotes:
        script_body = "\n".join(
            f"      flatpak remote-add --if-not-exists {nix_ind_esc(shlex.quote(r['name']))} {nix_ind_esc(shlex.quote(r['url']))}"
            for r in flatpak_remotes
        )
        prog_lines += [
            "  systemd.services.flatpak-repo = {",
            "    wantedBy = [ \"multi-user.target\" ];",
            "    path = [ pkgs.flatpak ];",
            "    script = ''",
            script_body,
            "    '';",
            "  };",
        ]
    packages = sorted(p["attr"] for p in data.get("packages", []) if p.get("enabled", True))
    if packages:
        pkg_lines = "\n".join(f"    {attr}" for attr in packages)
        prog_lines += [
            "  environment.systemPackages = with pkgs; [",
            pkg_lines,
            "  ];",
        ]
    if prog_lines or _has_section_brix(brix, "Programme"):
        return [_section("Programme")] + prog_lines
    return []


