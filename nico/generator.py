_NIX_ATTR_RE = re.compile(r"^[A-Za-z_][A-Za-z0-9_'-]*$")


def nix_esc(value) -> str:
    """Escape a value for use inside a double-quoted Nix string ("...")."""
    return (str(value)
            .replace("\\", "\\\\")
            .replace('"', '\\"')
            .replace("${", "\\${")
            .replace("\r", "")
            .replace("\n", "\\n")
            .replace("\t", "\\t"))


def nix_ind_esc(value) -> str:
    """Escape a value for use inside an indented Nix string (''...'')."""
    return str(value).replace("''", "'''").replace("${", "''${")


def nix_attr(name) -> str:
    """Render a name as a Nix attrpath component; quote when not a plain identifier."""
    s = str(name)
    return s if _NIX_ATTR_RE.match(s) else f'"{nix_esc(s)}"'


def _int_or(value, default: int) -> int:
    """Parse value as int; fall back to default instead of raising."""
    try:
        return int(str(value).strip())
    except (TypeError, ValueError):
        return default

# Matches nico-version lines in both formats:
#   # nico-version: a3f8c2d1          (legacy / no type)
#   # nico-version: co#a3f8c2d1       (with type code)
_VERSION_RE = re.compile(
    r'^# nico-version: (?:[a-z]+#)?([0-9a-f]{8})(?:#[pr])?\n', re.MULTILINE
)
# Matches only the legacy format (no type prefix) – used to detect missing type
_VERSION_RE_NOTYPE = re.compile(
    r'^# nico-version: ([0-9a-f]{8})\n', re.MULTILINE
)


def content_hash(content: str) -> str:
    """8-hex sha256 digest used by all nico-version lines."""
    return hashlib.sha256(content.encode()).hexdigest()[:8]


def _add_version_hash(content: str, ftype: str = "") -> str:
    """Insert '# nico-version: [type#]<hash>' on line 2 of content.
    The hash covers the content *without* the nico-version line itself.
    ftype: optional type code (e.g. 'co', 'fl') prepended as '<ftype>#'.
    """
    h = content_hash(content)
    prefix = f"{ftype}#" if ftype else ""
    lines = content.split("\n")
    lines.insert(1, f"# nico-version: {prefix}{h}")
    return "\n".join(lines)


def check_version_hash(file_content: str) -> str:
    """Check whether a NiCo-generated file has been externally modified.

    Returns:
      'ok'       – hash present and matches
      'modified' – hash present but doesn't match (file was edited outside NiCo)
      'no_hash'  – no nico-version line (file predates this feature or wasn't generated by NiCo)
    """
    m = _VERSION_RE.search(file_content)
    if not m:
        return "no_hash"
    stored = m.group(1)
    # Remove the entire nico-version line (with or without type prefix) to get canonical content
    canonical = _VERSION_RE.sub("", file_content, count=1)
    computed = content_hash(canonical)
    return "ok" if computed == stored else "modified"

# Width of the section marker line (total chars)
_MARKER_WIDTH = 78


def _section(name: str) -> str:
    """Return a section marker line:  # ── Name ──────────────────"""
    dashes = "─" * max(1, _MARKER_WIDTH - len(name) - 6)
    return f"\n  # ── {name} {dashes}"


def _has_section_brix(brix: dict, section: str) -> bool:
    """Return True if any brick block belongs to *section*."""
    return any(b.get("section") == section for b in brix.values())


