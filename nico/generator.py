def _sec_boot(data: dict, brix: dict) -> "list[str]":
    boot_loader         = data.get("boot_loader", "none") or "none"
    boot_kernel_params  = (data.get("boot_kernel_params", "") or "").strip()
    boot_initrd_systemd = data.get("boot_initrd_systemd", False)
    plymouth_enabled    = data.get("plymouth_enabled", False)
    plymouth_theme      = (data.get("plymouth_theme", "") or "").strip()
    if not (boot_loader != "none" or boot_kernel_params or boot_initrd_systemd
            or plymouth_enabled or _has_section_brix(brix, "Boot")):
        return []
    boot_efi_can_touch = data.get("boot_efi_can_touch", False)
    boot_efi_mount     = (data.get("boot_efi_mount_point", "/boot") or "/boot").strip()
    boot_config_limit  = _int_or(data.get("boot_config_limit"), 5)
    lines = [_section("Boot")]
    if boot_loader != "none":
        if boot_loader == "systemd-boot":
            lines += [
                "  boot.loader.systemd-boot.enable = true;",
                f"  boot.loader.systemd-boot.configurationLimit = {boot_config_limit};",
            ]
        elif boot_loader == "grub":
            lines += ["  boot.loader.grub.enable = true;"]
        lines += [f'  boot.loader.efi.canTouchEfiVariables = {"true" if boot_efi_can_touch else "false"};']
        if boot_efi_mount != "/boot":
            lines += [f'  boot.loader.efi.efiSysMountPoint = "{nix_esc(boot_efi_mount)}";']
    if boot_kernel_params:
        params = " ".join(f'"{nix_esc(p)}"' for p in boot_kernel_params.split())
        lines += [f"  boot.kernelParams = [ {params} ];"]
    if boot_initrd_systemd:
        lines += ["  boot.initrd.systemd.enable = true;"]
    if plymouth_enabled:
        lines += ["  boot.plymouth.enable = true;"]
        if plymouth_theme:
            lines += [f'  boot.plymouth.theme = "{nix_esc(plymouth_theme)}";']
    return lines


def _sec_system(data: dict, *, host_mode: bool) -> "list[str]":
    hostname = (data.get("hostname") or "").strip()
    if host_mode:
        # Sparse host file: no stateVersion, section only when set
        if not hostname:
            return []
        return [_section("System"), f'  networking.hostName = "{nix_esc(hostname)}";']
    state_version = (data.get("state_version") or "").strip()
    lines = [_section("System")]
    if hostname:
        lines += [f'  networking.hostName = "{nix_esc(hostname)}";']
    lines += [
        f'  system.stateVersion = "{nix_esc(state_version)}";' if state_version
        else '  # system.stateVersion = "";  # TODO: set NixOS state version'
    ]
    return lines


